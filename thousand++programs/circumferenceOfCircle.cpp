#include <iostream>
using namespace std;

// Wap to find the circumference of a circle with radius 10 in C++ 
int main(){
    int radius = 10;
    float pi = 3.1415;
    float area_circumference = 2 * pi * radius;
    cout << "Circumference of cricle is : " << area_circumference << endl;
    // -- output -- // 
    // Circumference of cricle is : 62.83
    return 0;
}