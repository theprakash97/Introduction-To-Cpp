#include <iostream>
using namespace std;

int main(){

    // C++ followed ASCII Encoding Standard 
    char sym1 = 'a';
    char sym2 = 'A';
    char sym3 = '#';
    char sym4 = '0';

    cout << (int)sym1 << endl; // 97
    cout << (int)sym2 << endl; // 65
    cout << (int)sym3 << endl; // 35
    cout << (int)sym4 << endl; // 48

    return 0;
}